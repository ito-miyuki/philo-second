#include "philo.h"

void	ft_putendl_fd(char *str, int fd)
{
	int	i;

	if (str)
	{
		i = 0;
		while (str[i] != '\0')
		{
			write(fd, &str[i], 1);
			i++;
		}
		write(fd, "\n", 1);
	}
}
